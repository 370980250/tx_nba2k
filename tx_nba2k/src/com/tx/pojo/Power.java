package com.tx.pojo;

import org.springframework.stereotype.Component;
//权限类
@Component
public class Power {
	//权限id
	private int id;
	//权限名
	private String pname;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getPname() {
		return pname;
	}
	public void setPname(String pname) {
		this.pname = pname;
	}
	
}
